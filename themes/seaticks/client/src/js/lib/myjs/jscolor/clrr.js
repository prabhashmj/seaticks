function changecolor(o){
	let v=o;
	let obj=canvas.getActiveObject();
	if(obj){
			obj.setFill(v);
			canvas.renderAll();

	       }
    else
	  {
		 canvas.backgroundColor =v;
	   	   canvas.renderAll();
	  }
}

